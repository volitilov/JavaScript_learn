// assert :::::::::::::::::::::::::::::::::::::::::::::::::::::::
assert(value)
// проверяет что value является true в логическом контексте.

assert.equal(value1, value2)
// проверяет равенство value1 == value2.

assert.strictEqual(value1, value2)
// проверяет строгое равенство value1 === value2.

assert.notEqual, assert.notStrictEqual
// проверки, обратные двум предыдущим.

assert.isTrue(value)
// проверяет, что value === true

assert.isFalse(value)
// проверяет, что value === false


